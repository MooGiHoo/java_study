package Calculator;

public class Calculator {
    public Calculator() {

    }
    void PowerON() {
        System.out.println("시작합니다.");
    }
    int plus(int x, int y) {
        return x+y;
    }

    double divide (int x, int y) {
        return x/y;
    }

    void PowerOff() {
        System.out.println("종료합니다.");
    }
}
